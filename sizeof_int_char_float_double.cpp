#include<iostream>
using namespace std;

int main()
{
	char a;
	cout<<"Size of char: "<<sizeof(a)<<" byte\n";
	int b;
	cout<<"Size of int: "<<sizeof(b)<<" bytes\n";
	float c;
	cout<<"Size of float: "<<sizeof(c)<<" bytes\n";
	double d;
	cout<<"Size of double: "<<sizeof(d)<<" bytes\n";
	return 0;
}	
